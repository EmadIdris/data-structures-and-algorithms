  console.log('(){}[[]]', seeIfValid('(){}[[]]'));
//-------------------------------------------------------------

  console.log('{}{Emad}[Idris](())', seeIfValid('{}{Emad}[Idris](())'));
//-------------------------------------------------------------

  console.log('[({}]', seeIfValid('[({}]'));
//-------------------------------------------------------------

  console.log('(](', seeIfValid('(]('));
